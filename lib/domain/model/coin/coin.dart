class Coin {
  final String id;
  final int symbolsCount;
  final String name;
  final String website;
  final double hourlyVolume;
  final double dailyVolume;
  final double monthlyVolume;

  Coin(
    this.id,
    this.symbolsCount,
    this.name,
    this.website,
    this.hourlyVolume,
    this.dailyVolume,
    this.monthlyVolume,
  );
}